#For point (r,z), calculate the q value
def d_flame_fitting(d_original, height_original, layer_thickness):
    Height=np.max(height_original)
    layerNum=math.ceil(Height/layer_thickness)
    H_array=np.linspace(0,Height,layerNum)

    x=height_original
    y=d_original
    exponential_number=8
    f1 = np.polyfit(x[1:len(x)-1], y[1:len(y)-1],  exponential_number)
    d_flame=np.polyval(f1, H_array)
    d_flame[d_flame < 0] = 0.01
    return d_flame


def heat_flux_v(d_flame, height_original, z_height, layer_thickness):
    #z：观测点高度
    #d_flame:火焰直径，array
    #Height:火焰高度
    #L:火焰高度
    Height=np.max(height_original)
    layerNum=math.ceil(Height/layer_thickness)
    H_array=np.linspace(0,Height,layerNum)
    Qv_total=0    
    k=-3.674
    z=z_height
    i=0
    L=Height
    #r表示圆柱中心线到目标微元的水平距离
    for i in range (0,layerNum):
        H=H_array[i]
        S=2*L/d_flame[i]
        h1=2*(H+10-z)/d_flame[i]
        h2=2*(H-z)/d_flame[i]
        A1=(h1**2+S**2+1)/(2*S)
        A2=(h2**2+S**2+1)/(2*S)
        #EQ(13)
        Fv1=1/(pi*S)*atan(h1/sqrt(S**2-1))-h1/(pi*S)*atan(sqrt((S-1)/(S+1)))+A1*h1/(pi*S*sqrt(A1**2-1))*atan(sqrt((A1+1)/(A1-1)*(S-1)/(S+1)))
        Fv2=1/(pi*S)*atan(h2/sqrt(S**2-1))-h2/(pi*S)*atan(sqrt((S-1)/(S+1)))+A2*h2/(pi*S*sqrt(A2**2-1))*atan(sqrt((A2+1)/(A2-1)*(S-1)/(S+1)))
        Fv=Fv1-Fv2
        #print(Fv)
        T=600 #tempreture
        #T=tempCal(H)
        epsilon=1-math.e**(k*d_flame[i]/100) #发射率
        E=sigma*epsilon*(T**4)

        qv=Fv*E 
        Qv_total=Qv_total+qv #EQ(6)
    return Qv_total
    #print(Qv_total)

def draw_rad_heat_flux_curve_Fv(d_flame, height_original, layer_thickness):
    #d_flame:火焰直径，array
    z_height=int(np.max(height_original))
